            {
                DataType = typeof(string),
                ColumnName = "DateTimeCreation"
            };
            dtStatusCopy.Columns.Add(dtColumnDateTimeCreationFordtStatusCopy);

            DataColumn dtColumnDateTimeLastModificationFordtStatusCopy = new()
            {
                DataType = typeof(string),
                ColumnName = "DateTimeLastModification"
            };
            dtStatusCopy.Columns.Add(dtColumnDateTimeLastModificationFordtStatusCopy);

            DataColumn dtColumnUserCreationIdFordtStatusCopy = new()
            {
                DataType = typeof(string),
                ColumnName = "UserCreationId"
            };
            dtStatusCopy.Columns.Add(dtColumnUserCreationIdFordtStatusCopy);

            DataColumn dtColumnUserLastModificationIdFordtStatusCopy = new()
            {
                DataType = typeof(string),
                ColumnName = "UserLastModificationId"
            };
            dtStatusCopy.Columns.Add(dtColumnUserLastModificationIdFordtStatusCopy);

            DataColumn dtColumnNameFordtStatusCopy = new()
            {
                DataType = typeof(string),
                ColumnName = "Name"
            };
            dtStatusCopy.Columns.Add(dtColumnNameFordtStatusCopy);

            
            #endregion

            dtStatusOriginal = dtStatus;

            foreach (DataRow DataRow in dtStatusOriginal.Rows)
            {
                dtStatusCopy.Rows.Add(DataRow.ItemArray);
            }

            var Sheet = Book.Worksheets.Add(dtStatusCopy);

            Sheet.ColumnsUsed().AdjustToContents();

            Book.SaveAs(path);
        }

